function SWEP:DealDamage( anim )
	local tr = util.TraceLine( {
		start = self.Owner:GetShootPos(),
		endpos = self.Owner:GetShootPos() + self.Owner:GetAimVector() * self.HitDistance,
		filter = self.Owner
	} )

	if ( !IsValid( tr.Entity ) ) then 
		tr = util.TraceHull( {
			start = self.Owner:GetShootPos(),
			endpos = self.Owner:GetShootPos() + self.Owner:GetAimVector() * self.HitDistance,
			filter = self.Owner,
			mins = Vector( -10, -10, -8 ),
			maxs = Vector( 10, 10, 8 )
		} )
	end

	if ( tr.Hit ) then 
		self.Weapon:EmitSound( "weapons/knife/knife_hitwall1.wav", 80, math.Rand(70, 80) )
	end

	 if (tr.Entity:IsPlayer() or tr.Entity:IsNPC() or tr.Entity.Type == "nextbot") then 
		self.Weapon:EmitSound( "weapons/knife/knife_hit1.wav", 80, math.Rand(55, 65) )
	end


	if IsValid(tr.Entity) and ( tr.Entity:IsNPC() or tr.Entity:IsPlayer() or tr.Entity:GetClass() == "prop_flimsy" or tr.Entity:GetClass() == "func_breakable" or tr.Entity.Type == "nextbot" or tr.Entity:Health() > 0 ) then
		local dmginfo = DamageInfo()
		if tr.Entity:IsPlayer() then
			dmginfo:SetDamage(self.Primary.PlayerDamage)
		else
			dmginfo:SetDamage(self.Primary.Damage)
		end
		dmginfo:SetDamageForce( self.Owner:GetRight() * 425 + self.Owner:GetForward() * 94 ) -- Yes we need those specific numbers
		dmginfo:SetInflictor( self )
		local attacker = self.Owner
		if ( !IsValid( attacker ) ) then attacker = self end
		dmginfo:SetAttacker( attacker )

		tr.Entity:TakeDamageInfo( dmginfo )
	end
end

